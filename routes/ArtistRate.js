var express = require('express');
var router = express.Router();
var ArtistRate=require('../models/ArtistRate');
 
router.get('/:id?',function(req,res,next){
 
if(req.params.id){
 
ArtistRate.getArtistRateById(req.params.id,function(err,rows){
 
if(err)
  {
  res.json({"status" : 500, "error" : err});
  }
  else{
  res.json({"status" : 200, "response" : rows});
  }
  });
 }
 else{
 
ArtistRate.getAllArtistRates(function(err,rows){
 
if(err)
  {
  res.json({"status" : 500, "error" : err});
  }
  else
  {
  res.json({"status" : 200, "response" : rows});
  }
 
 });
 }
 });
 router.get('/Artist/:id?',function(req,res,next){
	if(req.params.id){
		ArtistRate.getAllArtistRatesByArtistId(req.params.id,function(err,rows){
		if(err)
		  {
		  res.json({"status" : 500, "error" : err});
		  }
		  else{
		  res.json({"status" : 200, "response" : rows});
		  }
		  });
	}
 });
 router.post('/',function(req,res,next){
 
ArtistRate.addArtistRate(req.body,function(err,count){
  if(err)
  {
  res.json({"status" : 500, "error" : err});
  }
  else{
  res.json({"status": 200, "response": "Successfully created"});//or return count for 1 &amp;amp;amp; 0
  }
  });
 });
 router.delete('/:id',function(req,res,next){
 
ArtistRate.deleteArtistRate(req.params.id,function(err,count){
 
if(err)
  {
  res.json({"status" : 500, "error" : err});
  }
  else
  {
  res.json({"status": 200, "response": "Successfully deleted"})
  }
 
});
 });
 router.put('/:id',function(req,res,next){
 
ArtistRate.updateArtistRate(req.params.id,req.body,function(err,rows){
 
if(err)
  {
  res.json({"status" : 500, "error" : err});
  }
  else
  {
  res.json({"status" : 200, "response" : rows});
  }
  });
 });
 module.exports=router;